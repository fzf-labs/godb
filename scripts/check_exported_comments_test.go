func TestIsCheckedGoFile(t *testing.T) {
	tests := []struct {
		path string
		want bool
	}{
		{path: "api.go", want: true},
		{path: "api_test.go", want: false},
		{path: "api.gen.go", want: false},
		{path: "api.pb.go", want: false},
		{path: "README.md", want: false},
	}

	for _, tt := range tests {
		t.Run(tt.path, func(t *testing.T) {
			if got := isCheckedGoFile(tt.path); got != tt.want {
				t.Fatalf("isCheckedGoFile(%q)=%v want %v", tt.path, got, tt.want)
			}
		})
	}
}

func writeTestFile(t *testing.T, root, name, content string) {
	t.Helper()
	path := filepath.Join(root, name)
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		t.Fatalf("mkdir test file dir: %v", err)
	}
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatalf("write test file: %v", err)
	}
}

func containsSubstring(values []string, substr string) bool {
	for _, value := range values {
		if strings.Contains(value, substr) {
			return true
		}
	}
	return false
}
