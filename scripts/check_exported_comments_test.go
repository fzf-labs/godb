func TestMissingExportedCommentsReportsProductionAPI(t *testing.T) {
	root := t.TempDir()
	writeTestFile(t, root, "api/api.go", `package api

type MissingType struct{}

func MissingFunc() {}

const MissingConst = 1

var MissingVar = 2
`)

	missing, err := missingExportedComments(root)
	if err != nil {
		t.Fatalf("missing comments: %v", err)
	}

	for _, want := range []string{
		"exported type MissingType",
		"exported func MissingFunc",
		"exported const MissingConst",
		"exported var MissingVar",
	} {
		if !containsSubstring(missing, want) {
			t.Fatalf("expected %q in missing comments: %#v", want, missing)
		}
	}
}

func TestMissingExportedCommentsAcceptsDocumentedProductionAPI(t *testing.T) {
	root := t.TempDir()
	writeTestFile(t, root, "api/api.go", `package api

// DocumentedType has a doc comment.
type DocumentedType struct{}

// DocumentedFunc has a doc comment.
func DocumentedFunc() {}

// DocumentedConst has a doc comment.
const DocumentedConst = 1

// DocumentedVar has a doc comment.
var DocumentedVar = 2
`)

	missing, err := missingExportedComments(root)
	if err != nil {
		t.Fatalf("missing comments: %v", err)
	}
	if len(missing) != 0 {
		t.Fatalf("expected no missing comments, got %#v", missing)
	}
}

func TestMissingExportedCommentsSkipsGeneratedAndTestFiles(t *testing.T) {
	root := t.TempDir()
	writeTestFile(t, root, "api/generated.go", `// Code generated by test. DO NOT EDIT.
package api

type GeneratedType struct{}
`)
	writeTestFile(t, root, "api/api_test.go", `package api

type TestFixture struct{}

func (TestFixture) SchemaName(string) string { return "" }

func TestHelper() {}
`)
	writeTestFile(t, root, filepath.Join("orm", "example", "gorm", "postgres", "gorm_gen_repo", "repo.go"), `package repo

type GeneratedExampleType struct{}
`)

	missing, err := missingExportedComments(root)
	if err != nil {
		t.Fatalf("missing comments: %v", err)
	}
	if len(missing) != 0 {
		t.Fatalf("expected generated and test files to be skipped, got %#v", missing)
	}
}

